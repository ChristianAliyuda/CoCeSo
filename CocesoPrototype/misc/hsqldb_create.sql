CREATE TABLE Vorfall (
vid INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
vstart VARCHAR(30) NOT NULL, 
vende VARCHAR(30) NOT NULL,
vtyp VARCHAR(30) NOT NULL,
vtext VARCHAR(30) NOT NULL,
vstatus VARCHAR(30) NOT NULL,
);

CREATE TABLE Einheit (
eid INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
vid INTEGER,
ename VARCHAR(30) NOT NULL, 
etyp VARCHAR(30) NOT NULL,
estatus VARCHAR(30) NOT NULL,
FOREIGN KEY (vid) REFERENCES Vorfall(vid)
);
